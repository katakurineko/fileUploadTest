package jp.co.mediaseek.training.entity;

public class Meishi {

  private String name;
  private String yomigana;
  private String tel;
  private String mail;
  private String company;
  private String position;
  private String department;
  private String address;
  private String url;
  private String picturePath;
  private String memo;
  private int meishiId;


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getYomigana() {
    return yomigana;
  }

  public void setYomigana(String yomigana) {
    this.yomigana = yomigana;
  }

  public String getTel() {
    return tel;
  }

  public void setTel(String tel) {
    this.tel = tel;
  }

  public String getMail() {
    return mail;
  }

  public void setMail(String mail) {
    this.mail = mail;
  }

  public String getCompany() {
    return company;
  }

  public void setCompany(String company) {
    this.company = company;
  }

  public String getPosition() {
    return position;
  }

  public void setPosition(String position) {
    this.position = position;
  }

  public String getDepartment() {
    return department;
  }

  public void setDepartment(String department) {
    this.department = department;
  }

  public String getAddress() {
    return address;
  }

  public void setAddress(String address) {
    this.address = address;
  }

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }

  public String getPicturePath() {
    return picturePath;
  }

  public void setPicturePath(String picturePath) {
    this.picturePath = picturePath;
  }

  public String getMemo() {
    return memo;
  }

  public void setMemo(String memo) {
    this.memo = memo;
  }

  public int getMeishiId() {
    return meishiId;
  }

  public void setMeishiId(int meishiId) {
    this.meishiId = meishiId;
  }
}
